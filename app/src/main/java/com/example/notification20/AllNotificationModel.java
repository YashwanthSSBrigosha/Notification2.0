package com.example.notification20;

public class AllNotificationModel {

    String title,date,time;

    public AllNotificationModel(String title, String date, String time) {
        this.title = title;
        this.date = date;
        this.time = time;
    }

    public String getTitle() {
        return title;
    }

    public String getDate() {
        return date;
    }

    public String getTime() {
        return time;
    }
}
